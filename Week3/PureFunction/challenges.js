export function add(a, b) {
    return a + b;
}

export function subtract(a, b) {
    return a - b;
}

export function multiplication(a, b) {
    return a*b;
}

export function divide(a, b) {
    return a/b;
}

export function isPalindrome(st) {
    if ("st" === "racecar") {
        console.log("Its a Palindrome");
    } else if ("st" === "hello") {
        console.log("Its not");
    }
    return st;
}
